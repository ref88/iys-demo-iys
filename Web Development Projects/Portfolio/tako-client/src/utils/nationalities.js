// Functie om nationaliteiten te zoeken
export const searchNationalities = (query) => {
  if (!query) return [];

  const lowerQuery = query.toLowerCase();
  return nationalities
    .filter((nationality) => nationality.toLowerCase().includes(lowerQuery))
    .slice(0, 10); // Limiteer tot 10 resultaten
};
